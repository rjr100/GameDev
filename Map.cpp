Map::Map()
{
	dirt = TextureManager::LoadTexture("RawAssets/dirt.png");
	grass = TextureManager::LoadTexture("RawAssets/grass.png");
	water = TextureManager::LoadTexture("RawAssets/water.png");

	LoadMap(lvl1);

	src.x = src.y = 0;
	src.w = src.h = 32;
	dest.w = dest.h = 32;

	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[20][25]) 
{
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			map[row][col] = arr[row][col];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {

			type = map[row][col];
			dest.x = 32 * col;
			dest.y = 32 * row;

			switch (type) {
			case 0:
				TextureManager::Draw(water, src, dest);
				break;

			case 1:
				TextureManager::Draw(grass, src, dest);
				break;

			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;

			default:
				break;
			}

		}
	}
}